public class Student {
    private Integer age;
    private Integer gpa;

    public Student(Integer age, Integer gpa) {
        this.age = age;
        this.gpa = gpa;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getGpa() {
        return gpa;
    }

    public void setGpa(Integer gpa) {
        this.gpa = gpa;
    }
}
